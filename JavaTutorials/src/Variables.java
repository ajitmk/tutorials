
public class Variables {

	public static void main(String[] args) {
		//8  Primitive datatypes in Java
		
		byte myByte = 10;
		System.out.println("My byte valye "+ myByte);
		
		short myShort = 10000;
		System.out.println("My short vale "+ myShort);
		
		int myInt = 292929292;
		System.out.println("My int value"+ myInt);
		
		long myLong = 3838383;
		System.out.println("My long value"+ myLong);
		
		float myfloat = 20.0f;
		System.out.println("My float value" + myfloat);
		
		double mydouble = 21.0;
		System.out.println("My Double value" + mydouble);
		
		boolean myBoolen = true;
		System.out.println("My Boolean value is "+myBoolen);

		char myChar = 'a';
		System.out.println("My Char value is "+ myChar);
	}
	
}
